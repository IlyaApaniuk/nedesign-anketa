function opts(page, labels) {
  return labels.map((t, i) => ({
    t,
    img: `img/opt-p${String(page).padStart(2, '0')}-${String(i + 1).padStart(2, '0')}.jpg`,
  }));
}

function gallery(room, count) {
  return Array.from({ length: count }, (_, i) =>
    `img/${room}-${String(i + 1).padStart(2, '0')}.jpg`);
}

